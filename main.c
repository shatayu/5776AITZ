// drive code
task driveControl() {
	while (true) {
  	int leftPower = (abs(vexRT[Ch3] + vexRT[Ch1]) > 20) ? vexRT[Ch3] + vexRT[Ch1] : 0;
  	int rightPower = (abs(vexRT[Ch3] - vexRT[Ch1]) > 20) ? vexRT[Ch3] - vexRT[Ch1] : 0;
  	moveDrive(leftPower, rightPower);
  	wait1Msec(20);
	}
}

int conesOnMogo = 0;
task usercontrol() {
	// initialize drive code for top lift
	startTask(topLiftPI);

	// initialize drive code for main lift
	startTask(mainLiftPI);

	// initialize drive code for drive
	startTask(driveControl);

  while (true) {
  	// LCD code
  	displayLCDNumber(0, 8, conesOnMogo);

  	// mogo intake code
  	if (vexRT[Btn7U]) {
  		moveMogoIntake(-127); // withdraw mogo intake
  	} else if (vexRT[Btn7D]) {
  		moveMogoIntake(127); // extend mogo intake
  	} else {
  		moveMogoIntake(0); // power mogo intake off
  	}

  	// cone intake (claw) code
  	if (vexRT[Btn6U] && vexRT[Btn6D]) {
  		moveConeIntake(30); // stall torque
  	} else if (vexRT[Btn6U]) {
  		moveConeIntake(70); // close cone intake
  	} else if (vexRT[Btn6D]) {
  		moveConeIntake(-70); // open cone intake
  	} else {
  		moveConeIntake(0); // power claw off
  	}

  	// autostack
  	if (vexRT[Btn8R]) {
  		stopTask(topLiftPI);
  		stopTask(mainLiftPI);
  		autoStack(conesOnMogo, false);
  		reset();
  		conesOnMogo++;
  		moveTopLift(-127);
	  	wait1Msec(200);
	  	moveTopLift(0);
  		startTask(topLiftPI);
  		startTask(mainLiftPI);
  	}

  	// cone counter management
  	if (vexRT[Btn7L]) {
  		waitUntil(!vexRT[Btn7L]);
  		if (conesOnMogo > 0)
  			conesOnMogo--;
  	}

  	// cone counter management
  	if (vexRT[Btn7R]) {
  		waitUntil(!vexRT[Btn7R]);
  		conesOnMogo++;
  	}


  	if (vexRT[Btn8UXmtr2]) {
  		stopTask(topLiftPI);
  		stopTask(mainLiftPI);
  		autoStack(conesOnMogo, true);
  		matchLoadReset();
  		conesOnMogo++;
  		startTask(topLiftPI);
  		startTask(mainLiftPI);
  	}

  	if (vexRT[Btn8RXmtr2]) {
			autonMainLift_SWAG(2000, 10000);
  	}

  	wait1Msec(20);
  }
}
