int selectedAuton = 2;

task selector() {
	int NUM_AUTONS = 4;
	string autonNames[] = {"ONLY DEPLOY", "2PT STAGO", "24PT MOGO", "PSKILLS", "22PT MOGO"};

	while (!bIfiAutonomousMode) {
		// change autons by using buttons; left and right cycle through autons, center inverts direction
		if (nLCDButtons == 1) {
			waitUntil(nLCDButtons == 0);
			selectedAuton--;
			} else if (nLCDButtons == 2) {
			waitUntil(nLCDButtons == 0);
			selectedAuton *= -1;
			} else if (nLCDButtons == 4) {
			waitUntil(nLCDButtons == 0);
			selectedAuton++;
		}

		// loop over to the other end of the list if no more autons to go through
		if (abs(selectedAuton) > NUM_AUTONS) {
			selectedAuton = -sgn(selectedAuton) * NUM_AUTONS;
		}

		// display auton name
		displayLCDCenteredString(0, autonNames[abs(selectedAuton)]);

		// display auton side
		if (sgn(selectedAuton) == 1) {
			displayLCDCenteredString(1, "RIGHT");
			} else if (sgn(selectedAuton) == -1) {
			displayLCDCenteredString(1, "LEFT");
			} else {
			displayLCDCenteredString(1, "N/A");
		}
	}
}

void pre_auton() {
	startTask(selector);
	calibrate();
	bStopTasksBetweenModes = true;
	bLCDBacklight = true;
}

int sign;
task autonomous() {
	sign = sgn(selectedAuton);
	if (selectedAuton == 0) {
		// deploy
		moveDrive(500, 500);
		wait1Msec(500);
		moveDrive(0, 0);
		} else if (abs(selectedAuton) == 1) {
		stationaryGoal(sgn(selectedAuton));
		} else if (abs(selectedAuton) == 2) {
		mogo24(sgn(selectedAuton));
		} else if (abs(selectedAuton) == 3) {
		displayLCDCenteredString(0, "PSKILLS");
		pskills(sgn(selectedAuton));
		} else if (abs(selectedAuton) == 4){
		mogo22(sgn(selectedAuton));
	}
}

// drive code
int leftPower;
int rightPower;
task driveControl() {
	while (true) {
		// dead zone
		leftPower = (abs(vexRT[Ch3] + vexRT[Ch1]) > 20) ? vexRT[Ch3] + vexRT[Ch1] : 0;
		rightPower = (abs(vexRT[Ch3] - vexRT[Ch1]) > 20) ? vexRT[Ch3] - vexRT[Ch1] : 0;

		// apply power
		moveDrive(leftPower, rightPower);
		wait1Msec(20);
	}
}

task autostackControl() {
	while (true) {
		if (vexRT[Btn8L] || vexRT[Btn8LXmtr2])
			abortAutostack();

		if (vexRT[Btn8RXmtr2]) {
			startTask(matchReset);
			waitUntil(SensorValue[MainLiftPot] < 1500);
			abortAutostack();
		}

		if (SensorValue[MainLiftPot] < 1350) {
			a.stacked = false;
		}
		wait1Msec(20);
	}
}

int conesOnMogo = 0;

task usercontrol() {
	startTask(selector);
	// initialize drive code for drive
	startTask(driveControl);

	// initialize autostack stopping code
	startTask(autostackControl);

	int clawStall = 0;
	while (true) {
		// LCD code
		//displayLCDNumber(0, 8, conesOnMogo);

		// main lift code
		if (vexRT[Btn7U]) {
			stopTask(mainLiftPI);
			moveMainLift(127);
		} else if (vexRT[Btn7D]) {
			stopTask(mainLiftPI);
			moveMainLift(-127);
		} else {
			int mainLiftPower = (abs(vexRT[Ch3Xmtr2]) > 10) ? vexRT[Ch3Xmtr2] : 0;
			if (mainLiftPower != 0) {
				stopTask(mainLiftPI);
			}
			moveMainLift(mainLiftPower);
		}

		// top lift code
		if (vexRT[Btn8U]) {
			stopTask(topLiftPI);
			moveTopLift(127);
			} else if (vexRT[Btn8D]) {
			stopTask(topLiftPI);
			moveTopLift(-127);
			} else {
			int topLiftPower = (abs(vexRT[Ch2Xmtr2]) > 10) ? vexRT[Ch2Xmtr2] : 0;
			if (topLiftPower != 0) {
				stopTask(topLiftPI);
			}
			moveTopLift(topLiftPower);
		}

		// mogo intake code
		if ((vexRT[Btn5U] || vexRT[Btn7UXmtr2])) {
			moveMogoIntake(127); // withdraw mogo intake
			} else if ((vexRT[Btn5D] || vexRT[Btn7DXmtr2])) {
			moveMogoIntake(-127); // extend mogo intake
			} else {
			moveMogoIntake(0);
		}


		// cone intake (claw) code
		if (vexRT[Btn6U] && vexRT[Btn6D]) {
			moveConeIntake(15); // stall torque
		} else if (vexRT[Btn6U] || vexRT[Btn6UXmtr2]) {
			moveConeIntake(127); // close cone intake
		} else if (vexRT[Btn6D] || vexRT[Btn6DXmtr2]) {
			moveConeIntake(-127); // open cone intake
		} else {
			moveConeIntake(0); // stall cone intake in the right direction
		}

		// autostack
		if (vexRT[Btn8R] || vexRT[Btn8R]) {
			autostack(conesOnMogo, FIELD);
			conesOnMogo++;
		}

		// counter control
		if ((vexRT[Btn7L] || vexRT[Btn7LXmtr2]) && conesOnMogo > 0) {
			waitUntil(!vexRT[Btn7L] || vexRT[Btn7LXmtr2]);
			conesOnMogo--;
			} else if (vexRT[Btn7R] || vexRT[Btn7RXmtr2]) {
			waitUntil(!(vexRT[Btn7R] || vexRT[Btn7RXmtr2]));
			conesOnMogo++;
		}

		/*
		PARTNER STICK EXTRAS
		*/

		if (vexRT[Btn8UXmtr2]) {
			autostack(conesOnMogo, MATCH);
			conesOnMogo++;
		}

		if (vexRT[Btn8DXmtr2]) {
			conesOnMogo = 0;
		}

		if (vexRT[Btn8LXmtr2]) {
			abortAutostack();
		}

		wait1Msec(20);
	}
}
