task usercontrol() {
	int conesOnMogo = 0;
  while (true) {
  	// drive code
  	moveDrive(vexRT[Ch3] - vexRT[Ch1], vexRT[Ch3] + vexRT[Ch1]);
  	moveDrive(vexRT[Ch3Xmtr2] - vexRT[Ch1Xmtr2], vexRT[Ch3Xmtr2] + vexRT[Ch1Xmtr2]);

  	// top lift (4-bar) code
  	if (vexRt[Btn6U] || vexRT[Btn6UXmtr2]) {
  		moveTopLift(127);
  	} else if (vexRT[Btn6D] || vexRT[Btn6DXmtr2]) {
  		moveTopLift(-127);
  	} else {
  		moveTopLift(0);
  	}

  	// main lift (RD4B) code
  	if (vexRT[Btn5U]) {
  		moveMainLift(127);
  	} else if (vexRT[Btn5D]) {
  		moveMainLift(-127);
  	} else {
  		moveMainLift(0);
  	}

  	// mogo intake code
  	if (vexRT[Btn7L] || vexRT[Btn7LXmtr2]) {
  		moveMogoIntake(127);
  	} else if (vexRT[Btn7R] || vexRT[Btn7RXmtr2]) {
  		moveMogoIntake(-127);
  	} else {
  		moveMogoIntake(0);
  	}

  	// cone intake (claw) code
  	if (vexRT[Btn7U] || vexRT[Btn7UXmtr2]) {
  		moveConeIntake(60);
  	} else if (vexRT[Btn7D] || vexRT[Btn7DXmtr2]) {
  		moveConeIntake(-60);
  	} else {
  		moveConeIntake(0);
  	}

  	// autostack code
  	if (vexRT[Btn8R]) {
  		autoStack(conesOnMogo);
  		conesOnMogo++;
  	}

  	// autostack count control
  	if (vexRT[Btn8UXmtr2]) {
  		conesOnMogo++;
  	} else if (vexRT[Btn8DXmtr2]) {
  		conesOnMogo--;
  	} else if (vexRT[Btn8RXmtr2]) {
  		conesOnMogo = 0;
  	}

  	wait1Msec(20);
  }
}
